// Define a function to randomize an image from a given image to sound map.
function randomizeImage(imageToSoundMap, container) {
  // Get a random image filename from the imageToSoundMap object.
  const imageFilenames = Object.keys(imageToSoundMap);
const randomImageFilename = imageFilenames[Math.floor(Math.random() * imageFilenames.length)];

// Create a new image element with the source set to the random image filename.
const imageElement = document.createElement("img");
imageElement.src = randomImageFilename;
imageElement.width = 250;
imageElement.height = 250;

// Get the sound filename associated with the random image filename.
const soundFilename = imageToSoundMap[randomImageFilename];

// Create a new audio element and set its source to the corresponding sound filename.
const audioElement = document.createElement("audio");
audioElement.src = soundFilename;

// Add a click event listener to the image element that plays the associated sound.
imageElement.addEventListener("click", () => {
audioElement.play();
});

// Clear the image container element and add the new image and audio elements.
container.innerHTML = "";
container.appendChild(imageElement);
container.appendChild(audioElement);
}

// Define a function to remove an image from a given image container.
function removeImage(container) {
// Get the image element from the container.
const imageElement = container.querySelector("img");

// Check if an image element exists.
if (imageElement) {
// Remove the image element from the container.
container.removeChild(imageElement);
}
}

